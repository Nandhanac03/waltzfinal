			$(document).ready(function() {
				// Generate reCAPTCHA token on page load
				grecaptcha.ready(function() {
					grecaptcha.execute("<?= config_item('SITE_KEY'); ?>", {
						action: "contact"
					}).then(function(token) {
						$("#recaptchaResponse").val(token);
					});
				});

				$("#contact-form").on("submit", function(e) {
					e.preventDefault();
					e.stopImmediatePropagation();

					// Reset validation classes
					$(this).find('.form-control').removeClass('is-valid is-invalid');

					// Get form data
					var formData = {
						contact_name: $('#contact_name').val().trim(),
						contact_email: $('#contact_email').val().trim(),
						contact_phone: $('#contact_phone').val().trim(),
						contact_subject: $('#contact_subject').val().trim(),
						contact_message: $('#contact_message').val().trim(),
						'g-recaptcha-response': $('#recaptchaResponse').val()
					};

					// Client-side validation
					var isValid = true;
					var emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;

					// Validate name
					if (!formData.contact_name) {
						$('#contact_name').addClass('is-invalid');
						isValid = false;
					} else {
						$('#contact_name').addClass('is-valid');
					}

					// Validate email
					if (!formData.contact_email || !emailRegex.test(formData.contact_email)) {
						$('#contact_email').addClass('is-invalid');
						isValid = false;
					} else {
						$('#contact_email').addClass('is-valid');
					}

					// Validate phone
					if (!formData.contact_phone) {
						$('#contact_phone').addClass('is-invalid');
						isValid = false;
					} else {
						$('#contact_phone').addClass('is-valid');
					}

					// Validate subject
					if (!formData.contact_subject) {
						$('#contact_subject').addClass('is-invalid');
						isValid = false;
					} else {
						$('#contact_subject').addClass('is-valid');
					}

					// Validate message
					if (!formData.contact_message) {
						$('#contact_message').addClass('is-invalid');
						isValid = false;
					} else {
						$('#contact_message').addClass('is-valid');
					}

					// Proceed with AJAX if valid
					if (isValid) {
						// Show loader
						$('.pbmit-btn.submit .form-btn-loader').removeClass('d-none');
						$('.pbmit-btn.submit .pbmit-button-text').text('Sending...');

						$.ajax({
							url: "<?= base_url('contact_us/send'); ?>",
							type: "POST",
							data: formData,
							dataType: "json",
							success: function(response) {
								// Hide loader
								$('.pbmit-btn.submit .form-btn-loader').addClass('d-none');
								$('.pbmit-btn.submit .pbmit-button-text').text('Send');

								if (response.status === "success") {
									$("#response").html('<div class="alert alert-success">Your message has been sent successfully!</div>');
									$("#contact-form")[0].reset();
									$(this).find('.form-control').removeClass('is-valid is-invalid');
									// Refresh reCAPTCHA token
									grecaptcha.execute("<?= config_item('SITE_KEY'); ?>", {
										action: "contact"
									}).then(function(token) {
										$("#recaptchaResponse").val(token);
									});
								} else {
									$("#response").html('<div class="alert alert-danger">' + response.message + '</div>');
								}
							},
							error: function() {
								// Hide loader
								$('.pbmit-btn.submit .form-btn-loader').addClass('d-none');
								$('.pbmit-btn.submit .pbmit-button-text').text('Send');
								$("#response").html('<div class="alert alert-danger">Something went wrong, please try again.</div>');
							}
						});
					}
				});

				// Real-time validation
				$('#contact_name').on('input', function() {
					if ($(this).val().trim()) {
						$(this).removeClass('is-invalid').addClass('is-valid');
					} else {
						$(this).removeClass('is-valid').addClass('is-invalid');
					}
				});

				$('#contact_email').on('input', function() {
					var emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
					if (emailRegex.test($(this).val().trim())) {
						$(this).removeClass('is-invalid').addClass('is-valid');
					} else {
						$(this).removeClass('is-valid').addClass('is-invalid');
					}
				});

				$('#contact_phone').on('input', function() {
					if ($(this).val().trim()) {
						$(this).removeClass('is-invalid').addClass('is-valid');
					} else {
						$(this).removeClass('is-valid').addClass('is-invalid');
					}
				});

				$('#contact_subject').on('input', function() {
					if ($(this).val().trim()) {
						$(this).removeClass('is-invalid').addClass('is-valid');
					} else {
						$(this).removeClass('is-valid').addClass('is-invalid');
					}
				});

				$('#contact_message').on('input', function() {
					if ($(this).val().trim()) {
						$(this).removeClass('is-invalid').addClass('is-valid');
					} else {
						$(this).removeClass('is-valid').addClass('is-invalid');
					}
				});
			});
